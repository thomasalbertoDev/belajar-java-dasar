package operasiMatematika;

public class OperasiMatematika {

    public static void main(String[] args) {
        // TODO: Operasi Matematika
        int a = 10;
        int b = 5;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        // TODO: Augmented Assignment (Operasi yang dilakukan pada variabel dirinya sendiri)
        int c = 100;

        c += 10; // c = c + 10
        System.out.println(c);

        c -= 10; // c = c - 10
        System.out.println(c);

        c *= 10; // c = c * 10
        System.out.println(c);

        // TODO: Unary Operator (Operasi yang dilakukan pada variabel dirinya sendiri)
        int d = 100;

        d++; // d = d + 1
        System.out.println(d);

        d--; // d = d - 1
        System.out.println(d);

         
    }
}
