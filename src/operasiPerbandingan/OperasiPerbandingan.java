package operasiPerbandingan;

public class OperasiPerbandingan {
    public static void main(String[] args) {
        // TODO: Operasi Perbandingan
        int a = 10;
        int b = 5;

        System.out.println(a > b); // true
        System.out.println(a < b); // false

        System.out.println(a >= b); // true
        System.out.println(a <= b); // false

        System.out.println(a == b); // false
        System.out.println(a != b); // true

        
    }
}
